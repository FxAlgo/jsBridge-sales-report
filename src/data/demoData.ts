import { DataTable, DateGroupingType, recordNumberToDate } from "./fetch";

export function demoData(dataset: DataTable, type: DateGroupingType) {
	if (type === "year") {
		return dataset === "order" ? demoOrders : demoInvoices;
	} else if (type === "quarter") {
		return demoOrdersQ;
	} else {
		return demoOrdersM;
	}
}

function generateDemoData(type: DateGroupingType): string[][] {
	const data = [];
	const from = recordNumberToDate(type);
	const now = new Date();
	const thisYear = now.getFullYear();

	for (let year = from.getFullYear(); year <= thisYear; year++) {
		const lastMonth = year === thisYear ? now.getMonth() + 1 : 12;
		for (let month = 1; month <= lastMonth; month++) {
			data.push([year, month, fakeData(1)[0]]);
		}
	}
	return data;
}

function fakeData(count: number, min: number = 0, max: number = 1000000): number[] {
	const arr = [];
	for (let i = 0; i < count; i++) {
		arr.push(Math.floor(Math.random() * (max - min + 1)) + min);
	}
	return arr;
}

